#include <bits/stdc++.h>
using namespace std;

int main()
{
  char i, p, P, z, Z, e, E, d, D;
  cin>>i;
  
  if(i=='p' || i=='P')
    cout<<"PrepBytes";
  
  
  else if(i=='z' || i=='Z')
    cout<<"Zenith";
  

  else if(i=='e' || i=='E')
    cout<<"Expert Coder";
  
    
  else if(i=='d' || i=='D')
    cout<<"Data Structure";
  
    
  else
    cout<<"Wrong Input";
  
    
  return 0;
}